    private void jButton26ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton26ActionPerformed
        System.out.println("gui正在执行添加用户操作");
        udi = new UserDAOImp();
        //1 获取组件的信息 并进行类型转换
        String uname = jTextField20.getText().trim();
        char ch[] = jPasswordField1.getPassword();
        String password = new String(ch);
        String sex = "";
        if (jRadioButton1.isSelected()) {
            sex = "男";
        }
        if (jRadioButton2.isSelected()) {
            sex = "女";
        }
        int age = 0;
        try {
            String agestr = jTextField21.getText();    //字符串
            age = Integer.parseInt(agestr);
        } catch (NumberFormatException e) {
            e.printStackTrace();
        }
        String idk = jTextField22.getText().trim();
        String freeze = jTextField23.getText().trim();

        //如果属性超过三个，建议使用对象来封装传递数据
        User u = new User();
        u.setName(uname);
        u.setAge(age);
        u.setPassword(password);
        u.setSex(sex);
        u.setId_card(idk);
        u.setFreeze(freeze);

        //2、调用数据访问层方法
        boolean result = udi.insert(u);
        if (result) {
            System.out.println("gui添加用户成功");
            this.setVisible(false);
            //系统主界面
        } else {
            System.out.println("gui添加用户失败");

        }
    }//GEN-LAST:event_jButton26ActionPerformed

